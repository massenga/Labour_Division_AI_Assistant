import urllib.parse

with tab2:
    st.header("Search for Similar Cases on TanzLII")
    query = st.text_input("Enter case description (e.g., 'unfair termination due to pregnancy')")

    if query:
        encoded_query = urllib.parse.quote(query)
        search_url = f"https://tanzlii.org/search/?q={encoded_query}"
        
        st.markdown(
            f"<small><a href='{search_url}' target='_blank'>Click here to view full TanzLII search results for '{query}'</a></small>",
            unsafe_allow_html=True
        )

        # Attempt to extract 6 cases
        try:
            headers = {"User-Agent": "Mozilla/5.0"}
            resp = requests.get(search_url, headers=headers, timeout=10)
            soup = BeautifulSoup(resp.text, "html.parser")

            # Find Google CSE result blocks
            result_links = soup.select("a.gs-title")

            shown = 0
            st.markdown("### Top Matching Cases:")
            for tag in result_links:
                href = tag.get("href")
                title = tag.text.strip()
                if href and title and not href.startswith("https://webcache"):
                    st.markdown(f"- [{title}]({href})")
                    shown += 1
                if shown == 6:
                    break

            if shown == 0:
                st.warning("No case links found.")
        except Exception as e:
            st.error(f"Failed to load search results: {e}")


